package mx.com.convenios.bean;

import java.math.BigDecimal;

import org.apache.struts.action.ActionForm;

public class CampanaJsonRequest extends ActionForm {


	private int idCampana;
	private int idEmpresa;
	private String nombre;
	private int idSubEmpresa;
	private String fechaInicio;
	private String fechaVigencia;
	private BigDecimal montoMinimo;
	private boolean capturaDatos;	
	private String tipoMonedero;
	private int cantidadMonedero;
	private int montoMonedero;

	public CampanaJsonRequest() {
		super();
	}

	public CampanaJsonRequest(int idCampana, int idEmpresa, String nombre, int idSubEmpresa, String fechaInicio,
			String fechaVigencia, BigDecimal montoMinimo, boolean capturaDatos, String tipoMonedero,
			int cantidadMonedero, int montoMonedero) {
		super();
		this.idCampana = idCampana;
		this.idEmpresa = idEmpresa;
		this.nombre = nombre;
		this.idSubEmpresa = idSubEmpresa;
		this.fechaInicio = fechaInicio;
		this.fechaVigencia = fechaVigencia;
		this.montoMinimo = montoMinimo;
		this.capturaDatos = capturaDatos;
		this.tipoMonedero = tipoMonedero;
		this.cantidadMonedero = cantidadMonedero;
		this.montoMonedero = montoMonedero;
	}

	public int getIdEmpresa() {
		return idEmpresa;
	}

	public void setIdEmpresa(int idEmpresa) {
		this.idEmpresa = idEmpresa;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public int getIdSubEmpresa() {
		return idSubEmpresa;
	}

	public void setIdSubEmpresa(int idSubEmpresa) {
		this.idSubEmpresa = idSubEmpresa;
	}

	public String getFechaInicio() {
		return fechaInicio;
	}

	public void setFechaInicio(String fechaInicio) {
		this.fechaInicio = fechaInicio;
	}

	public String getFechaVigencia() {
		return fechaVigencia;
	}

	public void setFechaVigencia(String fechaVigencia) {
		this.fechaVigencia = fechaVigencia;
	}

	public BigDecimal getMontoMinimo() {
		return montoMinimo;
	}

	public void setMontoMinimo(BigDecimal montoMinimo) {
		this.montoMinimo = montoMinimo;
	}

	public boolean isCapturaDatos() {
		return capturaDatos;
	}

	public void setCapturaDatos(boolean capturaDatos) {
		this.capturaDatos = capturaDatos;
	}

	public String getTipoMonedero() {
		return tipoMonedero;
	}

	public void setTipoMonedero(String tipoMonedero) {
		this.tipoMonedero = tipoMonedero;
	}

	public int getCantidadMonedero() {
		return cantidadMonedero;
	}

	public void setCantidadMonedero(int cantidadMonedero) {
		this.cantidadMonedero = cantidadMonedero;
	}

	public int getMontoMonedero() {
		return montoMonedero;
	}

	public void setMontoMonedero(int montoMonedero) {
		this.montoMonedero = montoMonedero;
	}

	public int getIdCampana() {
		return idCampana;
	}

	public void setIdCampana(int idCampana) {
		this.idCampana = idCampana;
	}

	@Override
	public String toString() {
		return "CampanaJsonRequest [idCampana=" + idCampana + ", idEmpresa=" + idEmpresa + ", nombre=" + nombre
				+ ", idSubEmpresa=" + idSubEmpresa + ", fechaInicio=" + fechaInicio + ", fechaVigencia=" + fechaVigencia
				+ ", montoMinimo=" + montoMinimo + ", capturaDatos=" + capturaDatos + ", tipoMonedero=" + tipoMonedero
				+ ", cantidadMonedero=" + cantidadMonedero + ", montoMonedero=" + montoMonedero + "]";
	}




}
